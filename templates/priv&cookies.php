<h1>Política de Privacidad y Cookies</h1>
<p>Última actualización: 3 de junio de 2025</p>

<p>En KickAsh nos tomamos muy en serio tu privacidad. Esta política explica cómo recopilamos, usamos y protegemos tu información personal, así como el uso que hacemos de cookies y tecnologías similares.</p>

<h2>1. ¿Quién es el responsable del tratamiento de tus datos?</h2>
<p>KickAsh es responsable del tratamiento de tus datos personales. Puedes contactarnos en: <a href="mailto:privacidad@kickash.app">privacidad@kickash.app</a></p>

<h2>2. ¿Qué datos personales recogemos?</h2>
<ul>
    <li>Información de registro (nombre, correo electrónico, edad mínima)</li>
    <li>Datos de progreso, metas personales y mensajes dentro de la red</li>
    <li>Información técnica como dirección IP, tipo de dispositivo, sistema operativo</li>
    <li>Cookies y tecnologías similares (ver sección más abajo)</li>
</ul>

<h2>3. ¿Con qué finalidad usamos tus datos?</h2>
<ul>
    <li>Crear y mantener tu cuenta de usuario</li>
    <li>Ofrecerte herramientas de seguimiento de progreso y motivación</li>
    <li>Facilitar la interacción con otros usuarios</li>
    <li>Detectar comportamientos abusivos y mantener la seguridad de la plataforma</li>
    <li>Mejorar nuestros servicios y la experiencia del usuario</li>
</ul>

<h2>4. ¿Compartimos tus datos?</h2>
<p>No vendemos ni compartimos tus datos personales con terceros sin tu consentimiento, salvo cuando sea legalmente requerido o para funcionamiento técnico (como servidores en la nube).</p>

<h2>5. ¿Cuáles son tus derechos?</h2>
<p>De acuerdo con la normativa vigente, tienes derecho a:</p>
<ul>
    <li>Acceder a tus datos personales</li>
    <li>Rectificarlos si son incorrectos</li>
    <li>Solicitar su eliminación ("derecho al olvido")</li>
    <li>Oponerte al tratamiento de tus datos</li>
    <li>Portar tus datos a otro proveedor</li>
</ul>
<p>Para ejercer estos derechos, escribe a: <a href="mailto:kickash@kickash.ovh" class="fw">kickash@kickash.ovh</a></p>

<h2>6. Seguridad</h2>
<p>Utilizamos medidas técnicas y organizativas adecuadas para proteger tu información contra accesos no autorizados, pérdida o destrucción.</p>

<h2>7. Política de Cookies</h2>
<p>Utilizamos cookies propias y de terceros para:</p>
<ul>
    <li>Mejorar la experiencia de usuario</li>
    <li>Recordar preferencias de sesión</li>
    <li>Obtener estadísticas de uso anónimas</li>
</ul>

<p>Puedes configurar o rechazar el uso de cookies en la configuración de tu navegador. Al continuar usando KickAsh, aceptas el uso de cookies según esta política.</p>

<h2>8. Cambios en esta política</h2>
<p>Nos reservamos el derecho de modificar esta Política de Privacidad y Cookies en cualquier momento. Te avisaremos de cualquier cambio relevante mediante la app o correo electrónico.</p>

<h2>9. Contacto</h2>
<p>Para cualquier pregunta relacionada con esta política, contacta con nosotros en <a href="mailto:kickash@kickash.ovh" class="fw">kickash@kickash.ovh</a></p>