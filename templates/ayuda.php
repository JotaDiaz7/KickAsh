<h1>Centro de Ayuda</h1>
<p>Bienvenido a la sección de ayuda de KickAsh. Aquí encontrarás respuestas a las preguntas más comunes y consejos para sacar el máximo provecho a la aplicación.</p>

<h2>Preguntas Frecuentes (FAQ)</h2>

<details>
    <summary>¿Qué es KickAsh?</summary>
    <p>KickAsh es una aplicación/red social diseñada para ayudarte a dejar de fumar con apoyo de la comunidad, seguimiento de progreso, logros motivacionales y consejos personalizados.</p>
</details>

<details>
    <summary>¿Es KickAsh gratuita?</summary>
    <p>Sí, KickAsh es completamente gratuita en su versión actual. Podrían incluirse funciones premium en el futuro, pero siempre tendrás acceso a las funciones básicas sin coste.</p>
</details>

<details>
    <summary>¿Es necesario registrarse?</summary>
    <p>Sí, para poder interactuar con la comunidad, guardar tu progreso y recibir motivación personalizada, necesitas crear una cuenta gratuita.</p>
</details>

<details>
    <summary>¿Puedo usar KickAsh si ya he dejado de fumar?</summary>
    <p>¡Claro! Puedes usar KickAsh para mantener tu motivación, ayudar a otros usuarios con tu experiencia y seguir celebrando tus logros.</p>
</details>

<details>
    <summary>¿KickAsh sustituye ayuda médica profesional?</summary>
    <p>No. KickAsh es una herramienta de apoyo social y motivacional. No sustituye terapia médica, farmacológica ni psicológica. Te animamos a consultar con un profesional de la salud si necesitas orientación médica.</p>
</details>

<details>
    <summary>¿Puedo contactar con el equipo de soporte?</summary>
    <p>Sí. Puedes escribirnos en cualquier momento a <a href="mailto:soporte@kickash.ovh" class="fw">soporte@kickash.ovh</a>. Estamos aquí para ayudarte.</p>
</details>

<details>
    <summary>¿Cómo elimino mi cuenta?</summary>
    <p>Puedes eliminar tu cuenta desde el menú de configuración de la aplicación. Si tienes problemas, también puedes escribirnos para que lo hagamos por ti.</p>
</details>

<details>
    <summary>¿Cómo funciona el sistema de logros y recompensas?</summary>
    <p>A medida que avanzas en tu camino sin fumar, KickAsh desbloquea logros visuales y notificaciones motivadoras para reforzar tu progreso y ayudarte a seguir adelante.</p>
</details>

<details>
    <summary>¿Mi información es privada?</summary>
    <p>Sí. Respetamos tu privacidad. Consulta nuestra <a href="/coorp?privacy" class="fw">Política de Privacidad</a> para saber más sobre cómo protegemos tus datos.</p>
</details>

